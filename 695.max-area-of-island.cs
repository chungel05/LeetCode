// @lc code=start
public partial class Solution
{
    public void DFSMaxAreaOfIsland(int[][] grid, int row, int col, ref int area)
    {
        if (row < 0 || col < 0 || row >= grid.Length || col >= grid[0].Length || grid[row][col] == 0)
            return;

        area++;
        grid[row][col] = 0;
        DFSMaxAreaOfIsland(grid, row + 1, col, ref area);
        DFSMaxAreaOfIsland(grid, row - 1, col, ref area);
        DFSMaxAreaOfIsland(grid, row, col + 1, ref area);
        DFSMaxAreaOfIsland(grid, row, col - 1, ref area);

    }

    public int MaxAreaOfIsland(int[][] grid)
    {
        int maxArea = 0;

        for (int i = 0; i < grid.Length; i++)
        {
            for (int j = 0; j < grid[0].Length; j++)
            {
                if (grid[i][j] != 0)
                {
                    int area = 0;
                    DFSMaxAreaOfIsland(grid, i, j, ref area);
                    maxArea = Math.Max(maxArea, area);
                }
            }
        }

        return maxArea;
    }
}
